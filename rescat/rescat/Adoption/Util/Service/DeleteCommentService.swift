import Foundation

struct DeleteCommentService: DeletableService, APIServie {    

    typealias NetworkData = DefaultData
    static let shareInstance = DeleteCommentService()
    
    //MARK: Delete - /api/care-posts/{idx}/comments/{comment-idx} (댓글 삭제)
    func deleteComment(idx: Int, c_id: Int, completion: @escaping (NetworkResult<Any>) -> Void) {
        
        let deleteURL = self.url("care-posts/\(idx)/comments/\(c_id)")
        
        delete(deleteURL, params: [:]) { (result) in
            switch result {
                
            case .success(let networkResult):
                switch networkResult.resCode {
                    
                case HttpResponseCode.getSuccess.rawValue : //200
                    completion(.networkSuccess(networkResult.resResult))
                    
                case HttpResponseCode.badRequest.rawValue : //400
                    completion(.badRequest)
                    
                case HttpResponseCode.accessDenied.rawValue : //401
                    completion(.accessDenied)
                    
                case HttpResponseCode.serverErr.rawValue : //500
                    completion(.serverErr)
                    
                default :
                    print("Success: \(networkResult.resCode)")
                    break
                }
                break
                
            case .error(let resCode):
                switch resCode {
                    
                case HttpResponseCode.badRequest.rawValue.description : //400
                    completion(.badRequest)
                    
                case HttpResponseCode.accessDenied.rawValue.description : //401
                    completion(.accessDenied)
                    
                default :
                    print("Error: \(resCode)")
                    break
                }
                break
                
            case .failure(_):
                completion(.networkFail)
                print("Fail: Network Fail")
            }
        }
        
    }
}
