import Foundation
import UIKit
extension UIColor {
    static func rescatBlack() -> UIColor{
        return UIColor(red: 74/255, green: 74/255, blue: 74/255, alpha: 1)
    }
    static func rescatPink() -> UIColor {
        return UIColor(red: 242/255, green: 145/255, blue: 145/255, alpha: 1)
    }
    static func rescatPer() -> UIColor {
        return UIColor(red: 255/255, green: 231/255, blue: 215/255, alpha: 1)
    }
    static func rescatGray() -> UIColor {
        return UIColor(red: 196/255, green: 196/255, blue: 196/255, alpha: 1)
    }
    static func rescatWhite() -> UIColor {
        return UIColor(red: 255/255, green: 245/255, blue: 240/255, alpha: 1)

    }
}
